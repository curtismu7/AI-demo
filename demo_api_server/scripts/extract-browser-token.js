#!/usr/bin/env node
// banking_api_server/scripts/extract-browser-token.js
/**
 * Extract the most recent live access token from the BFF session store (sessions.db)
 * and write it to .env.test-tokens so integration tests can use it without manual
 * copy-paste.
 *
 * Usage (from banking_api_server/):
 *   node scripts/extract-browser-token.js
 *
 * What it does:
 *   1. Opens data/sessions.db (SQLite, created by the BFF on first login)
 *   2. Finds the session with the most recently issued, unexpired access token
 *   3. Writes .env.test-tokens with INTEGRATION_SUBJECT_ACCESS_TOKEN=<token>
 *   4. Prints instructions for running the live integration tests
 *
 * The written file (.env.test-tokens) is gitignored — never committed.
 * Jest picks it up automatically via src/__tests__/setup/loadBrowserToken.js
 * when RUN_LIVE_TESTS=true or RUN_PINGONE_TOKEN_INTEGRATION=true is set.
 *
 * Prerequisites:
 *   - BFF server has been run at least once (creates sessions.db)
 *   - You have logged in via the browser (creates a session with oauthTokens)
 */

'use strict';

const path = require('path');
const fs   = require('fs');

const DB_PATH  = path.join(__dirname, '../data/sessions.db');
const OUT_PATH = path.join(__dirname, '../.env.test-tokens');

// ---------------------------------------------------------------------------
// SQLite — use the same better-sqlite3 the server uses
// ---------------------------------------------------------------------------
let Database;
try {
  Database = require('better-sqlite3');
} catch {
  console.error('[extract-browser-token] better-sqlite3 not available.');
  console.error('Run: npm install (from banking_api_server/)');
  process.exit(1);
}

if (!fs.existsSync(DB_PATH)) {
  console.error(`[extract-browser-token] sessions.db not found at ${DB_PATH}`);
  console.error('Start the BFF server and log in via the browser first.');
  process.exit(1);
}

const db = new Database(DB_PATH, { readonly: true });
const nowSec = Math.floor(Date.now() / 1000);

// Find the most recently issued session that has oauthTokens.accessToken and has not expired
const rows = db.prepare(`
  SELECT sid, sess, expire
  FROM sessions
  WHERE json_extract(sess, '$.oauthTokens.accessToken') IS NOT NULL
    AND expire > ?
  ORDER BY expire DESC
  LIMIT 10
`).all(nowSec);

db.close();

if (rows.length === 0) {
  console.error('[extract-browser-token] No valid sessions with access tokens found.');
  console.error('Log in via the browser at the Super Banking app, then re-run this script.');
  process.exit(1);
}

// Pick the session whose access token has the latest iat (most recently issued)
let best = null;
let bestIat = -1;

for (const row of rows) {
  let sess;
  try { sess = JSON.parse(row.sess); } catch { continue; }
  const token = sess?.oauthTokens?.accessToken;
  if (!token) continue;

  // Decode JWT payload (no verification — we just need iat)
  let iat = 0;
  try {
    const parts = token.split('.');
    if (parts.length === 3) {
      const payload = JSON.parse(Buffer.from(parts[1], 'base64url').toString());
      iat = payload.iat || 0;
    }
  } catch { /* ignore malformed */ }

  if (iat > bestIat) {
    bestIat = iat;
    best = { sess, token, row };
  }
}

if (!best) {
  console.error('[extract-browser-token] Could not decode any token from sessions.');
  process.exit(1);
}

const { sess, token } = best;
const email    = sess?.user?.email || sess?.user?.username || '(unknown)';
const issuedAt = bestIat ? new Date(bestIat * 1000).toISOString() : '(unknown)';

// Decode aud/scope for display
let aud = '', scope = '', exp = '';
try {
  const parts = token.split('.');
  if (parts.length === 3) {
    const p = JSON.parse(Buffer.from(parts[1], 'base64url').toString());
    aud   = Array.isArray(p.aud) ? p.aud.join(', ') : (p.aud || '');
    scope = p.scope || '';
    exp   = p.exp ? new Date(p.exp * 1000).toISOString() : '';
  }
} catch { /* ignore */ }

// Write .env.test-tokens
const out = [
  '# Auto-generated by scripts/extract-browser-token.js — DO NOT COMMIT',
  `# Session user: ${email}`,
  `# Token issued: ${issuedAt}`,
  `# Token expires: ${exp}`,
  `# Audience: ${aud}`,
  `# Scopes: ${scope}`,
  '',
  `INTEGRATION_SUBJECT_ACCESS_TOKEN=${token}`,
  'RUN_LIVE_TESTS=true',
  'RUN_PINGONE_TOKEN_INTEGRATION=true',
].join('\n') + '\n';

fs.writeFileSync(OUT_PATH, out, 'utf8');

console.log('');
console.log('✅  Token extracted from browser session');
console.log(`   User:    ${email}`);
console.log(`   Issued:  ${issuedAt}`);
console.log(`   Expires: ${exp}`);
console.log(`   Written: .env.test-tokens`);
console.log('');
console.log('Run live tests:');
console.log('  npm run test:live');
console.log('  npm run test:live:token    # token exchange only');
console.log('  RUN_LIVE_TESTS=true npm test -- --testPathPattern=dual-token-exchange-live --forceExit');
console.log('');
console.log('The token is automatically picked up — no manual env var export needed.');
console.log('');
